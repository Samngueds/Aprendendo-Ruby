=begin
1 - ABSTRAÇÃO
  Ação de abtrair uma entidade(coisa) do mundo real,
  e transforma-la em uma classe.
2 - ENCUPASULAMENTO 
  Ato de dividir um programa em divirsas partes, tornando-o 
  flexivel, facil de modifica e  incluir nova fucionalidade
3 - Herança
  Habilidade de cria uma classe com caracteristica de outra
  existente, A Herança promove o reuso e reaproveitamento de codigo.
4 - Poliformismo
  capacidade de utilizar um metodo de diferentes formas para
  diferentes objetos





