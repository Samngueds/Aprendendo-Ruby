A outras Regras que entram :

1 - Require
      Com ele e possivel carrega arquivos em ruby que foram escritos por  você
      Para exemplificar como isso acontece você criara um projeto onde alguns
      arquivos irão conter apenas uma classe


2 - Escopo das varieveis
      O escopo define onde a varivel vai esta dentro do programa
      Em Ruby exite quatro tipos:
      
      - variavel local 
          É declarada com a primeira letra do seu nome sendo
          letra minuscula oou sublinhado

          Pode ser acessadaapenas onde foi criada. por exemplo, se você
          definir uma variavel local dentro de uma classe
          apenas dentro desta classe, se a definiu dentro de um  metodo
          você so conseguira acessa - la apenas dentro desse metodo
          e assim por diante

      - variavel global
          Declarada com o  Prefixo $

          Ela  pode ser acessada em qualquer lugar do programa
          Seu  uso  e Formente desencorajado pois alem de ser visivel em
          qualquer lugar do codigo  tambem pode ser alterada em inumeros
          locais ocasioando dificuldades no rastreamento de bugs

      - variavel de classe
        Declarada com prefixo @@

        Pode ser acessada em  qualquer lugar da classe onde  foi declarada seu valor e
        compartilhado entre todas as instancias de sua classe.


      - variavel de instancias
        Seu  nome começa com o simbulo @

        Semelhante  a variavel de classe, tendo como a unica diferença o valor que não é
        compartilhado entre todas as instacias da sua classe

